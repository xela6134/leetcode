# for loops
import time

for i in range (10):
    print(i)
print("")

for i in range (10, 20):
    print(i)
print("")

for i in range (10, 20, 2):
    print(i)
print("")
    
for i in "What up mate":
    print(i)
print("")

for seconds in range(10, 0, -1):
    print(seconds)
    time.sleep(1)
print("Happy new year")